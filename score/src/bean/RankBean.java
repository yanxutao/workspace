package bean;

public class RankBean {
	private int intHigher;
	private int intEqual;
	private int intLower;
	private double doubleHigher;
	private double doubleEqual;
	private double doubleLower;
	
	public RankBean() {
	}
	
	public int getIntHigher() {
		return intHigher;
	}

	public void setIntHigher(int intHigher) {
		this.intHigher = intHigher;
	}

	public int getIntEqual() {
		return intEqual;
	}

	public void setIntEqual(int intEqual) {
		this.intEqual = intEqual;
	}

	public int getIntLower() {
		return intLower;
	}

	public void setIntLower(int intLower) {
		this.intLower = intLower;
	}
	
	public double getDoubleHigher() {
		return doubleHigher;
	}

	public void setDoubleHigher(double doubleHigher) {
		this.doubleHigher = doubleHigher;
	}

	public double getDoubleEqual() {
		return doubleEqual;
	}

	public void setDoubleEqual(double doubleEqual) {
		this.doubleEqual = doubleEqual;
	}

	public double getDoubleLower() {
		return doubleLower;
	}

	public void setDoubleLower(double doubleLower) {
		this.doubleLower = doubleLower;
	}
	

}
